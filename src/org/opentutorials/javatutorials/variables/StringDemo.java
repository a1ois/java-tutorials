package org.opentutorials.javatutorials.variables;

public class StringDemo {

	public static void main(String[] args) {
		String first; // first라는 String 형식 변수
		first = "coding";
		
		String second;
		second = "Life";
		
		System.out.println(first+ " " + second);
		
		String a, b;
		a = "code";
		b = "everybody";
		
		System.out.println(a+b);
	}

}